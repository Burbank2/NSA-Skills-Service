<script setup>
import { computed, ref } from 'vue'
import { useRouter } from 'vue-router'

const props = defineProps({
  options: Object,
  to: Object,
  buttonIdSuffix: {
    type: String,
    required: true
  }
})
const emit = defineEmits(['edit', 'delete', 'share', 'unshare'])
const router = useRouter()

let isReadOnlyProj = false

const shareBtnIcon = computed(() => {
  return props.options?.shareEnabled === true ? 'fas fa-hands-helping' : 'fas fa-handshake-alt-slash'
})

const shareTitle = computed(() => {
  return props.options?.shareEnabled === true ? `Share ${props.options?.type}` : `Unshare ${props.options?.type}`
})

const editBtn = ref();
const deleteBtn = ref();

const handleShareClick = () => {
  let eventName = 'share'
  if (props.options.shareEnabled === false) {
    eventName = 'unshare'
  }
  emit(eventName)
}

const handleManageClick = () => {
  if (props.to) {
    router.push(props.to)
  }
}

const focusOnEdit = () => {
  // editBtn.value.focus();
}

const focusOnDelete = () => {
  // deleteBtn.value.focus();
}

defineExpose({
  focusOnEdit,
  focusOnDelete,
})

</script>

<template>
  <div class="flex" :class="{ 'justify-content-center' : isReadOnlyProj }">
    <div class="">
      <SkillsButton
        size="small"
        outlined
        severity="info"
        @click="handleManageClick"
        :aria-label="`Manage ${options.type} ${options.name}`"
        icon="fas fa-arrow-circle-right"
        :label="isReadOnlyProj ? 'View' : 'Manage'"
        :data-cy="`manageBtn_${options.id}`">
      </SkillsButton>
    </div>

    <div v-if="!isReadOnlyProj" class="flex-1 text-right">
      <ButtonGroup>
        <SkillsButton
          v-if="options.showShare === true"
          :id="`shareBtn${buttonIdSuffix}`"
          ref="shareBtn"
          size="small"
          outlined
          severity="info"
          :icon="shareBtnIcon"
          @click="handleShareClick"
          :title="shareTitle" />
        <SkillsButton
          :id="`editBtn${buttonIdSuffix}`"
          ref="editBtn"
          icon="fas fa-edit"
          size="small"
          outlined
          severity="info"
          @click="emit('edit')"
          :track-for-focus="true"
          :title="`Edit ${options.type}`"
          :aria-label="`Edit ${options.type} ${options.name}`"
          role="button"
          label=""
          data-cy="editBtn" />

        <SkillsButton
          :id="`deleteBtn${buttonIdSuffix}`"
          variant="outline-primary"
          v-tooltip="options.deleteDisabledText"
          ref="deleteBtn"
          size="small"
          outlined
          severity="info"
          @click="emit('delete')"
          :disabled="options.isDeleteDisabled"
          :title="`Delete ${options.type}`"
          :track-for-focus="true"
          :aria-label="options.deleteDisabledText ? options.deleteDisabledText : `Delete ${options.type} ${options.name}`"
          role="button"
          label="" icon="text-warning fas fa-trash"
          data-cy="deleteBtn"></SkillsButton>
      </ButtonGroup>
    </div>
  </div>
</template>

