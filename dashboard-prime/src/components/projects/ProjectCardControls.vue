<script setup>
import { computed } from 'vue';
import { useStore } from 'vuex';

const store = useStore();
const props = defineProps(['project', 'isDeleteDisabled', 'deleteDisabledText', 'readOnlyProject'])
const emit = defineEmits(['edit-project', 'unpin-project', 'copy-project', 'delete-project'])

const isRootUser = computed(() => {
  return store.getters['access/isRoot'];
});

const focusOnEdit = () => {
  this.$refs.editBtn.focus();
};

const focusOnCopy = () => {
  this.$refs.copyBtn.focus();
};

const focusOnDelete = () => {
  this.$nextTick(() => this.$refs.deleteBtn.focus());
};
</script>

<template>
  <div class="flex justify-content-end flex-wrap">
    <SkillsButton
        :to="{ name:'Subjects', params: { projectId: project.projectId, project: project }}"
        size="small"
        class="mr-2"
        outlined
        severity="info"
        :data-cy="'projCard_' + project.projectId + '_manageBtn'"
        :label="readOnlyProject ? 'View' : 'Manage'"
        icon="fas fa-arrow-circle-right"
        :aria-label="'manage project + project.name'">
    </SkillsButton>
    <SkillsButton
      v-if="isRootUser"
      outlined
      severity="info"
      @click="emit('unpin-project')"
      data-cy="unpin"
      size="small"
      class="mr-2"
      aria-label="'remove pin for project '+ project.name"
      label="Unpin"
      icon="fas fa-ban"
      :aria-pressed="project.pinned">
    </SkillsButton>
      <div class="p-buttonset mr-2 p-0 flex" v-if="!readOnlyProject">
        <SkillsButton
          :id="`editPojBtn${project.projectId}`"
          :track-for-focus="true"
          ref="editBtn"
          outlined
          severity="info"
          size="small"
          @click="emit('edit-project', $event.target.value)"
          title="Edit Project"
          :aria-label="'Edit Project ' + project.name"
          role="button"
          icon="fas fa-edit"
          data-cy="editProjBtn" />

        <SkillsButton
          :id="`copyPojBtn${project.projectId}`"
          ref="copyBtn"
          outlined
          severity="info"
          :track-for-focus="true"
          size="small"
          @click="emit('copy-project', $event.target.value)"
          title="Copy Project"
          :aria-label="'Copy Project ' + project.name"
          role="button"
          icon="fas fa-copy"
          label=""
          data-cy="copyProjBtn"/>

          <SkillsButton
            :id="`deletePojBtn${project.projectId}`"
            outlined
            severity="info"
            ref="deleteBtn"
            size="small"
            :track-for-focus="true"
            class="p-text-secondary"
            @click="emit('delete-project', $event.target.value)"
            :disabled="isDeleteDisabled"
            v-tooltip="deleteDisabledText"
            title="Delete Project"
            :aria-label="'Delete Project ' + project.name"
            role="button"
            icon="text-warning fas fa-trash p-text-warning"
            data-cy="deleteProjBtn" />
      </div>

  </div>
</template>

